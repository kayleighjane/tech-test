describe("Caesar Cipher Encryption", function() {
   
 
    describe("Encrypt by ROT13 method by moving letter 13 places along in the alphabet", function(){
         
        
        it("A should be converted 13 places in alphabet to N", function() {
            expect(encrypt("A")).toEqual("N");
        });
 		it("Z should be converted 13 places in alphabet to M", function() {
            expect(encrypt("Z")).toEqual("M");
        });
		
           });
	
	    describe("Pass any character not in the alphabet", function(){
         
        
        it("Space should be passed in sentence", function() {
            expect(encrypt(" ")).toEqual(" ");
        });
 	
           });

	
	  });
  
		
